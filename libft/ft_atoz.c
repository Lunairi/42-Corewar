#include "libft.h"

size_t			ft_atoz(const char *str)
{
	int			i;
	size_t		out;

	i = 0;
	out = 0;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		out = out * 10 + (str[i] - '0');
		++i;
	}
	return (out);
}
